function ServicelistModel(parentModel) {
    SubModel.call(this, parentModel, ServicelistModelDefines);
    this.registerSubObject = function (loadType) {
        this.registerTableObject(ServicelistModelDefines.SL2_TVAPI_TABLE_FAVORITE_LIST_LIST, "createFavoritelistIterator");
        this.registerTableObject(ServicelistModelDefines.SL2_TVAPI_TABLE_SERVICELIST_LIST, "createServicelistIterator");
        //dbtag:20161025 - To block channels by setting attribute to biz start
            // SetServiceAttribute
            this.registerActionObject(
            ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_SERVICELIST_SET_SERVICE_ATTRIBUTE,
            [
                {name:"SetServiceAttribute",method:function(object,id,attr,value){
                    return object.invoke(id,attr,value);
                    }}
            ],null);
        //dbtag:20161025 - To block channels by setting attribute to biz end
        // skip/unskip   skip:type=1   unskip:type=0
        this.registerActionObject(ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_SERVICELIST_REMOVE, [{
            name: "RemoveChannel",
            method: function (object, folderUuid, type, ids) {
                var str = '"' + folderUuid + '",' + type + "," + ids.toString(); //dbtag:20160919 - change to fix the channel delete issues in UI
                DBG_INFO("uid:" + ids.toString());
                return eval("object.invoke(" + str + ")")
            }
        }], "RemoveChannelCallback");
        //dbtag:20161013 - add to restore channels in delete list start
        // Restore
        this.registerActionObject(ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_SERVICELIST_RESTORE, [{
             name:"RestoreChannel",method:function(object,folderUuid,ids){
                var str = "";
                for(var i =0;i<ids.length;i++){
                    str = str+'"'+ids[i]+'"'+',';
                }
                str = str+'"'+folderUuid+'"';
                return eval('object.invoke('+str+')');
            }
        }], "RestoreChannelCallback");
        //dbtag:20161013 - add to restore channels in delete list end
        // Rename (FAV1-FAV4) use (folderUuid1,folderUuid2,uid,name),  (Anolog/DVBC/DVBT) not use (folderUuid2)
        this.registerActionObject(ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_FAVOURITE_LIST_RENAME, [{
            name: "NewRenameFavChannel",
            method: function (object, folderUuid1, folderUuid2, uid, name) {
                var str = '"' + folderUuid1 + '",' + folderUuid2.toString() + "," + uid.toString() + ",";
                for (var j = 0; j < name.length; j++) {
                    if (j != name.length - 1) {
                        str = str + '"' + name[j].replace(/\\/g, "\\\\").replace(/\"/g, '\\"') + '",'
                    } else {
                        str = str + '"' + name[j].replace(/\\/g, "\\\\").replace(/\"/g, '\\"') + '"'
                    }
                }
                return eval("object.invoke(" + str + ")")
            }
        }], "NewRenameFavChannelCallback");
        // add fav channel:para1--from uid(Anolog/DVBC/DVBT);para2-- To uid(FAV1-FAV4);para3-- channel id in (FAV1-FAV4)
        this.registerActionObject(ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_FAVOURITE_LIST_ADD_LIST, [{
            name: "addFavouriteChannel",
            method: function (object, folderUid, favUid, ids) {
                var str = '"' + favUid + '",' + '"' + ids.toString() + '"';
                return eval("object.invoke(" + str + ")")
            }
        }], "addFavouriteChannelCallback");
        //remove the channels(contain fav) under a certain tunner(DVBS/DVBC/DVBT).
        this.registerActionObject(
        ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_FAVOURITE_LIST_REMOVE,
        [
            {name:"removeFavouriteList",method:function(object,uid){
                return object.invoke(uid);
            }}
        ],"removeFavouriteListCallback");
        /*
        // cancel fav channel:para1--To uid(FAV1---FAV4);para2--From uid(Anolog/DVBC/DVBT);para3--channel id in (Anolog/DVBC/DVBT)
        this.registerActionObject(ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_FAVOURITE_LIST_REMOVE, [{
            name: "removeFavouriteChannel",
            method: function (object, folderUid1, folderUid2, uid) {
                var str = '"' + folderUid1 + '",' + '"' + folderUid2.toString() + '",' + '"' + uid.toString() + '"';
                return eval("object.invoke(" + str + ")")
            }
        }], "cancelFavouriteChannelCallback");
        */
        //  FAV:  para2--destnum;para3--moveItemsNum;
        //  Anolog/DVBC/DVBT:  para2--destSvlRecID;  para3--moveItemsSvlRecID;
        this.registerActionObject(
            ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_SERVICELIST_MOVE,
            [
               //dbtag:20161010 - change to intergrate with the channel move interface in biz start
                {name:"MoveMoreChannel",method:function(object,folderUuid,objNum,type,ids){
                    var str = '"'+folderUuid+'",'+objNum+','+type+','+ids.toString();
                    return eval('object.invoke('+str+')');
                }}
               //dbtag:20161010 - change to intergrate with the channel move interface in biz end
            ],"MoveMoreChannelCallback"
        );
        // sort  flag  0:A-Z;   1:0-9(not use)
        this.registerActionObject(
            ServicelistModelDefines.SL2_TVAPI_TABLE_ACTION_SERVICELIST_SWAP,
            [
                {name:"SortByName",method:function(object,folderUuid,flag){
                    var str =  '"'+folderUuid+'",'+flag;
                    return eval('object.invoke('+str+')');
                }}
            ],"SortByNameCallback"
        );

        // ListImport
        this.registerActionObject(
            ServicelistModelDefines.SL2_TVAPI_ACTION_SERVICELIST_LIST_IMPORT,
            [
                {name:"ListImport",method:function(object){
                    return object.invoke();
                }}
            ],"null"
        );
        // ListExport
        this.registerActionObject(
            ServicelistModelDefines.SL2_TVAPI_ACTION_SERVICELIST_LIST_EXPORT,
            [
                {name:"ListExport",method:function(object){
                    return object.invoke();
                }}
            ],"null"
        );
        // ListImportStatus
        this.registerIntegerObject(
            ServicelistModelDefines.SL2_TVAPI_I32_SERVICELIST_LIST_IMPORT_STATUS,
            "getListImportStatus", "setListImportStatus", "onListImportStatusChaged",
            null, null );

        // ListExportStatus
        this.registerIntegerObject(
            ServicelistModelDefines.SL2_TVAPI_I32_SERVICELIST_LIST_EXPORT_STATUS,
            "getListExportStatus", "setListExportStatus", "onListExportStatusChaged",
            null, null );

        // ChannellistUpdateAuto
        this.registerIntegerObject(
            ServicelistModelDefines.SL2_TVAPI_I32_SERVICELIST_CHANNELLIST_UPDATE_AUTO,
            "getChannellistUpdateAuto", "setChannellistUpdateAuto", "onChannellistUpdateAutoChaged",
            null, null );
        // ChannellistNeedUpdate
        this.registerIntegerObject(
            ServicelistModelDefines.SL2_TVAPI_I32_SERVICELIST_CHANNELLIST_NEED_UPDATE,
            "getChannellistNeedUpdate", "setChannellistNeedUpdate", "onChannellistNeedUpdateChaged",
            null, null );
    }

}
